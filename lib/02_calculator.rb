def add nbOne, nbTwo
    return nbOne + nbTwo
end

def subtract nbOne, nbTwo
    return nbOne - nbTwo
end
def sum arr
    return arr.sum
end
def multiply nbOne, nbTwo
    return nbOne * nbTwo
end
def power nbOne
    return nbOne * nbOne
end

def factorial num
i = 1
fact = 1
    while i <= num  
        fact *= i
        i += 1
    end
    return fact

end
